    public void validateFields() {
        // set the user data
        tmp_user.setName(user_name.getText());
        tmp_user.setEmail(user_email.getText());
        tmp_user.setTelNo(user_tel.getText());
        tmp_user.setAddress(user_address.getText());
        tmp_user.setPassword(user_password.getText());
        tmp_user.setSecurityQuestion(user_question.getText());
        tmp_user.setAnswer(user_answer.getText());
        
        if (!tmp_user.getName().equals("") && tmp_user.getEmail().matches(emailPattern) && tmp_user.getTelNo().matches(mobileNumberPattern) && (tmp_user.getTelNo().length() == 10 || tmp_user.getTelNo().length() == 11) && !tmp_user.getPassword().equals("") && !tmp_user.getAddress().equals("") && !tmp_user.getSecurityQuestion().equals("") && !tmp_user.getAnswer().equals("")) 
        {
           saveBtn.setEnabled(true);
        }
        else 
        {
            saveBtn.setEnabled(false);
        }
    }

