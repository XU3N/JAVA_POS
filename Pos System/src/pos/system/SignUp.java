    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        registered_user = UserDao.checkEmailAndUsername(tmp_user.getEmail(), tmp_user.getName());
        if (!(registered_user == null) && registered_user.getStatus().equals("true") && (registered_user.getEmail().equals(tmp_user.getEmail()) || registered_user.getName().equals(tmp_user.getName()))) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"color:red\">Email or Username Already Taken!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        } 
        else if(!(registered_user == null) && registered_user.getStatus().equals("false") && registered_user.getEmail().equals(tmp_user.getEmail()))
        {
            ImageIcon icon = new ImageIcon("src/pos/system/img/file.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size: larger\">Please Wait For Admin Approval!</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE, icon);
        }
        else 
        {
            verified_user = tmp_user;
            verified_user.setStatus("false");
            try {
                UserDao.saveData(verified_user);
                clear();
            } catch (SQLException ex) {
                Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

