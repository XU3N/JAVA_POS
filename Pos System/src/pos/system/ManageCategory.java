    public void search_data(String search) {

        // search customer table
        String name = cat_search_table.getText();

        try {

            DefaultTableModel dt = (DefaultTableModel) cat_show_table.getModel();
            dt.setRowCount(0);

            Statement s = DatabaseConnect.dbconn().createStatement();

            ResultSet result = s.executeQuery(" SELECT * FROM category WHERE category_name LIKE '%" + name + "%'");

            while (result.next()) {

                Vector v = new Vector();

                v.add(result.getString(1));
                v.add(result.getString(2));

                dt.addRow(v);
            }
        } catch (SQLException e) {

        }
    }


