    private void cat_save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cat_save_btnActionPerformed
        // Save category data

        String categoryName = category_txt.getText();
        
        try {
            Category category = CategoryDao.checkRecord(categoryName);

            if (category == null) {
                category = new Category();
                category.setName(categoryName);
                CategoryDao.saveCategory(category);
            } else {
                ImageIcon icon = new ImageIcon("src/pos/system/img/delete.png");
                JOptionPane.showMessageDialog(null, "<html><b style=\"font-size: larger\">This category already exist!</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE, icon);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ManageCategory.class.getName()).log(Level.SEVERE, null, ex);
        }
        table_load();
    }//GEN-LAST:event_cat_save_btnActionPerformed

    private void cat_update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cat_update_btnActionPerformed
        // update customer data
        String id = cat_search.getValue().toString();
        Category category = new Category();
        category.setId((String) cat_search.getValue());
        category.setName(category_txt.getText());
        try {
            CategoryDao.updateCategory(category);
        } catch (SQLException ex) {
            Logger.getLogger(ManageCategory.class.getName()).log(Level.SEVERE, null, ex);
        }
        table_load();
    }//GEN-LAST:event_cat_update_btnActionPerformed

    private void cat_delete_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cat_delete_btnActionPerformed
        // delete customer data
        Category category = new Category();
        String id = cat_search.getValue().toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to Delete " + category.getName() + " Category?", "Select", JOptionPane.YES_NO_OPTION);

        try {
            if (a == 0) {
                CategoryDao.deleteCategory(id);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageCategory.class.getName()).log(Level.SEVERE, null, ex);
        }
        table_load();
    }//GEN-LAST:event_cat_delete_btnActionPerformed

