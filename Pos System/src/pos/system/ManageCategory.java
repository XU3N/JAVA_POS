    private void table_load() {
        DefaultTableModel dtm = (DefaultTableModel) cat_show_table.getModel();
        dtm.setRowCount(0);
        ArrayList<Category> list = CategoryDao.getAllRecords();
        for (Category categoryObj : list) {
            dtm.addRow(new Object[]{categoryObj.getId(), categoryObj.getName()});
        }
    }

    public void validateField() {
        String category = category_txt.getText().trim();
        if (!category.isEmpty() && !category.equals("Enter category name")) {
            cat_save_btn.setEnabled(true);
            cat_delete_btn.setEnabled(true);
            cat_update_btn.setEnabled(true);
        } else {
            cat_save_btn.setEnabled(false);
            cat_delete_btn.setEnabled(false);
            cat_update_btn.setEnabled(false);
        }
    }

